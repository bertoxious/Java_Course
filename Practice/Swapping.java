public class Swapping{
	public static void main(String [] args){
		int a = 45;
		int b = 95;
		int c = 100;
		int d = 40;
		int temp;
		temp = a;
		a = b;
		b = temp;
		temp = c;
		c = d;
		d = temp;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

		// Swapping
	}
}